constexpr i64 INF = INT_FAST64_MAX / 2;
constexpr f64 EPS = DBL_EPSILON;

void solve() {
    i64 l, x, y, s, d;
    cin >> l >> x >> y >> s >> d;
    f64 ans = (d + l - s) % l / (f64) (y + x);
    if (y - x > 0) ans = min(ans, (s + l - d) % l / (f64) (y - x));
    cout << ans << endl;
}

