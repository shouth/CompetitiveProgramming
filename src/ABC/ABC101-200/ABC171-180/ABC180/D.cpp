using i64 = int_fast64_t;
using u64 = uint_fast64_t;
using f64 = double;
using p64 = pair<i64, i64>;

constexpr i64 INF = INT_FAST64_MAX / 2;

void solve() {
    i64 X, Y, A, B;
    cin >> X >> Y >> A >> B;

    i64 i = 0, a = 1;
    i64 ans = 0;
    while (true) {
        ans = max(ans, i + (Y - 1 - (X * a)) / B);
        if (X * a >= Y / A) break;
        a *= A, i++;
    }
    cout << ans << endl;
}

int main() {
    cin.tie(nullptr);
    ios_base::sync_with_stdio(false);
    cout << fixed << setprecision(15);
    solve();
}
