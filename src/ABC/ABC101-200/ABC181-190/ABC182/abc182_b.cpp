using i64 = int_fast64_t;
using u64 = uint_fast64_t;
using f64 = double;
using p64 = pair<i64, i64>;

constexpr i64 INF = INT_FAST64_MAX / 2;

void solve() {
    i64 N;
    cin >> N;
    vector<i64> A(N);
    for (auto& e : A) cin >> e;

    i64 mcnt = 0, ans = 0;
    for (i64 x = 2; x <= 1000; x++) {
        i64 cnt = 0;
        for (auto& e : A) if (e % x == 0) cnt++;
        if (cnt > mcnt) mcnt = cnt, ans = x;
    }
    cout << ans << endl;
}

int main() {
    cin.tie(nullptr);
    ios_base::sync_with_stdio(false);
    cout << fixed << setprecision(15);
    solve();
}
