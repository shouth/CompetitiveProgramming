#define all(x) begin(x), end(x)

using i64 = int_fast64_t;
using f64 = double;

constexpr i64 INF = INT_FAST64_MAX / 2;
constexpr f64 EPS = DBL_EPSILON;

void solve() {
    i64 a, b, c, k;
    cin >> a >> b >> c >> k;

    i64 ans = 0;
    i64 d = min(a, k);
    ans += d;
    k -= d;
    d = min(b, k);
    k -= d;
    d = min(c, k);
    ans -= d;
    cout << ans << endl;
}

int main() {
    cin.tie(nullptr);
    ios_base::sync_with_stdio(false);
    cout << fixed << setprecision(15);
    solve();
}
