constexpr i64 INF = INT_FAST64_MAX / 2;
constexpr f64 EPS = DBL_EPSILON;

void solve() {
    string a;
    cin >> a;
    i64 n = a.size();

    i64 ans = n * (n - 1) / 2 + 1;
    map<char, i64> cnt;
    for (auto& e : a) ans -= cnt[e], cnt[e]++;
    cout << ans << endl;
}

